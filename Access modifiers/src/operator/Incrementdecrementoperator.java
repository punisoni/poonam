package operator;

public class Incrementdecrementoperator {

	public static void main(String[] args) {
		int a=50;
		int b=60;
		System.out.println("value of a  "+a);
		System.out.println("value of b  "+b);
 
	System.out.println("value of --a   "+(--a));
	System.out.println("value of a  "+a);
	System.out.println("value ofb-- "+(b--));
	System.out.println("value of b  "+b);
	
	
				
}

}
