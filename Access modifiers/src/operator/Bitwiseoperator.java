package operator;

public class Bitwiseoperator {

	public static void main(String[] args) 
	{
	int a=5;
	int b=7;
	System.out.println("AND" +(a&b));
	System.out.println("OR" +(a|b));
	System.out.println("XOR" +(a^b));
	System.out.println("Complement" +(~a));
	System.out.println("Right shift 5 devide by 2=" +(a>>1));
	System.out.println("Right shift 7 devide by 2=" +(b>>1));
	System.out.println("Left shift 5 multiply by 2=" +(a<<1));
	System.out.println("Left shift 7 multiply by 2=" +(b<<1));
	System.out.println("Right shift of 5 =" +(a>>>1));
	System.out.println("Right shift of 7=" +(b>>>1));






	

	}

}
